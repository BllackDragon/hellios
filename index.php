        <h2>Существует множество моделей данных объективов, отличающихся сборкой и компоновкой.</h2>
        <p class="pb" align="center">В основном, они отличаются такими вещами:</p>
        <ul>
            <li>заводом изготовителем</li>
            <li>посадочной резьбой или байонетом</li>
            <li>оправой корпуса</li>
            <li>наличием репетира диафрагмы</li>
            <li>наличием кольца предустановки диафрагмы</li>
            <li>наличием мультипросветления (маркировка 'МС')</li>
            <li>именами, написанными на латыни или кириллице</li>
            <li>И множеством других мелочей</li>
            </ul>

        <p class="pb" align="center">Объективы ГЕЛИОС-44 выпускались на нескольких заводах:</p>
<ol>
<li>КМЗ - Красногорский Механический Завод</li>
<li>ММЗ - Минский Механический Завод имени С. И. Вавилова, с 1971 ставший БелОМО (Белорусское Оптико-Механическое Объединение)</li>
<li>Оптико-механический завод "Юпитер", Валдай</li>
<li><a href="https://www.zenit.photo/?ysclid=lo9oos7l5p14091939" target="blank">Завод ‘Зенит’</a>,Вилейка</li>
</ol>
